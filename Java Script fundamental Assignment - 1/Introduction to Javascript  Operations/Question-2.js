// In JavaScript, operators are symbols that perform operations on operands. Operators can be categorized based on their functionality. Here are some common categories with examples:

// Arithmetic Operators: Perform mathematical operations.


let a = 5;
let b = 3;
let sum = a + b; // Addition
let difference = a - b; // Subtraction
let product = a * b; // Multiplication
let Division = a / b; // Division
let remainder = a % b; // Modulus


// Comparison Operators:Compare values and return a Boolean result.

let x = 10;
let y = 5;
console.log(x > y); // Greater than
console.log(x < y); // Less than
console.log(x >= y); // Greater than or equal to
console.log(x <= y); // Less than or equal to
console.log(x === y); // Equal to
console.log(x !== y); // Not equal to


// Logical Operators:Perform logical operations on Boolean values.

let isTrue = true;
let isFalse = false;
console.log(isTrue && isFalse); // Logical AND
console.log(isTrue || isFalse); // Logical OR
console.log(!isTrue); // Logical NOT


// Conditional (Ternary) Operator: A shorthand for an if-else statement.

let age = 20;
let eligibility = (age >= 18) ? "Eligible" : "Not eligible";
