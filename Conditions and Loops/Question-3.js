// Loops are control structures in programming that allow a set of instructions to be repeated multiple times. They are essential for automating repetitive tasks, iterating over data structures, and implementing various algorithms. Loops help in making code more concise, readable, and efficient.



// For Loop:

for (let i = 0; i < 5; i++) {
    console.log(i);
}


// While Loop

let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}
