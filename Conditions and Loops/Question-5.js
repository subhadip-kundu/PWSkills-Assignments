// Ascending order
console.log("Ascending Order:");
let countAsc = 1;
while (countAsc <= 25) {
    console.log(countAsc);
    countAsc++;
}



// Descending order
console.log("\nDescending Order:");
let countDesc = 25;
while (countDesc >= 1) {
    console.log(countDesc);
    countDesc--;
}
